import java.util.*;

public class AddTwoNumber{
public static void main(String[] args){
	Scanner sc = new Scanner(System.in);
	System.out.println("Enter First Number");
	int a = sc.nextInt();
	System.out.println("Enter Second Number");
	int b = sc.nextInt();
	int ans = a+b;
	System.out.println("Result : " + ans);
}
}